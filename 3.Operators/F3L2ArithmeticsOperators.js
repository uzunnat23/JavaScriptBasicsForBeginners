// In JS exist 5 operators: Arithmetic, Assigment, Comparison, Logical, Bitwise

// Arithmetic operators
let x = 10;
let y = 3;
let z = 5;
let m = 8;

console.log('Arithmetic operators');
console.log('x + y = ' + (x + y));
console.log('x - y = ' + (x - y));
console.log('x * y = ' + (x * y));
console.log('x + y = ' + (x + y));
console.log('x % y = ' + (x % y));
console.log('x ** y = ' + (x ** y));

